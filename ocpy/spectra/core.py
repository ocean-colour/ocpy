# SpectrumStack
# --------------------------------------------------------------------
@dataclass
class SpectrumStack:
    """ A collection of :class:`Spectrum`, possibly ragged.

    Generated by JXP and Claude.

    Attributes:
        spectra (list): List of :class:`Spectrum` instances.
    """
    spectra: list = field(default_factory=list)

    def __post_init__(self):
        """ Validate the members. Generated by JXP and Claude. """
        for s in self.spectra:
            if not isinstance(s, Spectrum):
                raise TypeError(
                    'SpectrumStack members must be Spectrum instances')

    # -- container protocol ------------------------------------------
    def __len__(self):
        """ Number of spectra. Generated by JXP and Claude. """
        return len(self.spectra)

    def __iter__(self):
        """ Iterate over member spectra. Generated by JXP and Claude. """
        return iter(self.spectra)

    def __getitem__(self, idx):
        """ Index / slice members. Generated by JXP and Claude. """
        return self.spectra[idx]

    def __repr__(self):
        """ Compact summary. Generated by JXP and Claude. """
        grid = 'gridded' if self.is_gridded else 'ragged'
        return f'<SpectrumStack n={len(self)} {grid}>'

    # -- gridding ----------------------------------------------------
    @property
    def is_gridded(self):
        """ True if all members share one wavelength grid.

        Generated by JXP and Claude.

        Returns:
            bool
        """
        return utils.common_grid([s.wavelength for s in self.spectra])

    def as_array(self):
        """ Return ``(wavelength, values_2d, errors_2d)`` for a gridded stack.

        Generated by JXP and Claude.

        Returns:
            tuple: ``wavelength`` (nwave,), ``values`` (nspec, nwave),
                ``errors`` (nspec, nwave). Members without errors contribute
                NaN rows.

        Raises:
            ValueError: If the stack is ragged (call :meth:`rebin` first).
        """
        if not self.is_gridded:
            raise ValueError(
                'Stack is ragged; rebin(wv_grid) onto a common grid before '
                'calling as_array().')
        if len(self.spectra) == 0:
            empty = np.zeros((0, 0))
            return np.zeros(0), empty, empty

        wv = self.spectra[0].wavelength
        values = np.vstack([s.values for s in self.spectra])
        errors = np.vstack([
            s.errors if s.errors is not None
            else np.full_like(s.values, np.nan)
            for s in self.spectra])
        return wv, values, errors

    def rebin(self, wv_grid, method='interp'):
        """ Rebin every member onto ``wv_grid`` -> a gridded stack.

        Generated by JXP and Claude.

        Args:
            wv_grid (np.ndarray): Target grid / bin edges (see
                :meth:`Spectrum.rebin`).
            method (str): ``'interp'`` or ``'bin'``.

        Returns:
            SpectrumStack: A new, gridded stack.
        """
        return SpectrumStack(
            [s.rebin(wv_grid, method=method) for s in self.spectra])

    # -- per-spectrum provenance accessors ---------------------------
    @property
    def dates(self):
        """ Per-spectrum dates. Generated by JXP and Claude. """
        return np.array([s.date for s in self.spectra])

    @property
    def lats(self):
        """ Per-spectrum latitudes. Generated by JXP and Claude. """
        return np.array([s.lat for s in self.spectra], dtype=float)

    @property
    def lons(self):
        """ Per-spectrum longitudes. Generated by JXP and Claude. """
        return np.array([s.lon for s in self.spectra], dtype=float)

    @property
    def depths(self):
        """ Per-spectrum depths. Generated by JXP and Claude. """
        return np.array([s.depth for s in self.spectra], dtype=float)

    @property
    def sources(self):
        """ Per-spectrum sources. Generated by JXP and Claude. """
        return np.array([s.source for s in self.spectra], dtype=object)

    # -- xarray / netCDF interop -------------------------------------
    def to_xarray(self):
        """ Convert to an ``xarray.Dataset`` (gridded or ragged).

        Because wavelength is a data variable (not a coordinate), ragged
        members are handled by NaN-padding every row to the longest member;
        the per-row ``wavelength`` variable records the true grid so the pad
        is unambiguous. Provenance is stored as a JSON list-of-dicts in
        ``.attrs['provenance_json']`` (netCDF-safe, fully round-trippable).

        Generated by JXP and Claude.

        Returns:
            xarray.Dataset: dims ``(spectrum, channel)``.
        """
        import xarray

        nspec = len(self.spectra)
        maxlen = max((len(s) for s in self.spectra), default=0)

        wv = np.full((nspec, maxlen), np.nan)
        val = np.full((nspec, maxlen), np.nan)
        err = np.full((nspec, maxlen), np.nan)
        prov = []
        for i, s in enumerate(self.spectra):
            n = len(s)
            wv[i, :n] = s.wavelength
            val[i, :n] = s.values
            if s.errors is not None:
                err[i, :n] = s.errors
            # Reuse the single-spectrum attrs builder for each member.
            prov.append(s._provenance_attrs())

        ds = xarray.Dataset(
            data_vars=dict(
                wavelength=(('spectrum', 'channel'), wv),
                values=(('spectrum', 'channel'), val),
                errors=(('spectrum', 'channel'), err),
            ),
        )
        ds.attrs['provenance_json'] = json.dumps(prov)
        return ds

    @classmethod
    def from_xarray(cls, ds):
        """ Build a :class:`SpectrumStack` from an ``xarray.Dataset``.

        Generated by JXP and Claude.

        Args:
            ds (xarray.Dataset): As produced by :meth:`to_xarray`.

        Returns:
            SpectrumStack
        """
        prov = json.loads(ds.attrs.get('provenance_json', '[]'))
        wv_all = np.asarray(ds['wavelength'].values, dtype=float)
        val_all = np.asarray(ds['values'].values, dtype=float)
        err_all = np.asarray(ds['errors'].values, dtype=float)

        spectra = []
        nspec = wv_all.shape[0]
        for i in range(nspec):
            wv = wv_all[i]
            # Drop the NaN pad using the wavelength row as the mask.
            good = np.isfinite(wv)
            attrs = prov[i] if i < len(prov) else {}
            has_err = bool(int(attrs.get('has_errors', 1)))
            err = err_all[i][good] if has_err else None
            kwargs = Spectrum._provenance_from_attrs(attrs)
            spectra.append(Spectrum(
                wavelength=wv[good], values=val_all[i][good],
                errors=err, **kwargs))
        return cls(spectra)

    def to_netcdf(self, path, **kwargs):
        """ Write the stack to netCDF. Generated by JXP and Claude.

        Args:
            path (str): Output path.
            **kwargs: Passed to ``xarray.Dataset.to_netcdf``.
        """
        self.to_xarray().to_netcdf(path, **kwargs)

    @classmethod
    def read_netcdf(cls, path):
        """ Read a stack from netCDF. Generated by JXP and Claude.

        Args:
            path (str): Input path.

        Returns:
            SpectrumStack
        """
        import xarray
        with xarray.open_dataset(path) as ds:
            ds = ds.load()
        return cls.from_xarray(ds)
