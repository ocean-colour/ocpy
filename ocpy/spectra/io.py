def from_tara(row, flavor='ap'):
    """ Build a :class:`Spectrum` from one Tara Oceans row.

    Reuses :func:`ocpy.tara.spectra.spectrum_from_row`. The ``ap`` / ``cp``
    families are in 1/m. Latitude/longitude/date are pulled from the row when
    present (column names vary between Tara tables, so they are looked up
    defensively).

    Generated by JXP and Claude.

    Args:
        row (pandas.Series): One row of a Tara Oceans table.
        flavor (str): Spectrum flavor, ``'ap'`` or ``'cp'`` (default
            ``'ap'``).

    Returns:
        Spectrum
    """
    from ocpy.tara import spectra as tara_spectra

    wv, values, errs = tara_spectra.spectrum_from_row(row, flavor=flavor)

    # Defensive provenance lookup across a few candidate column names.
    lat = _get(row, 'lat')
    if lat is None:
        lat = _get(row, 'Latitude')
    lon = _get(row, 'lon')
    if lon is None:
        lon = _get(row, 'Longitude')

    # Prefer a real timestamp column; the integer 'date' (YYYYMMDD) is a
    # fallback that must be parsed explicitly (np.datetime64 would otherwise
    # misread the integer).
    dt = _get(row, 'datetime')
    if dt is not None:
        date = np.datetime64(dt)
    else:
        raw = _get(row, 'date')
        date = (np.datetime64(pandas.to_datetime(str(int(raw)),
                                                 format='%Y%m%d'))
                if raw is not None else None)

    # Ancillary fields (cruise, salinity, water temperature) -> metadata.
    metadata = {'flavor': flavor}
    for key in ('cruise', 'sal', 'Wt'):
        val = _get(row, key)
        if val is not None:
            metadata[key] = val

    return Spectrum(
        wavelength=np.asarray(wv, dtype=float),
        values=np.asarray(values, dtype=float),
        errors=np.asarray(errs, dtype=float),
        units='1/m',
        source='Tara',
        date=date,
        lat=lat,
        lon=lon,
        metadata=metadata,
    )


def stack_from_tara(tbl, flavor='ap'):
    """ Build a :class:`SpectrumStack` from a Tara Oceans table.

    Generated by JXP and Claude.

    Args:
        tbl (pandas.DataFrame): A Tara Oceans table.
        flavor (str): Spectrum flavor (default ``'ap'``).

    Returns:
        SpectrumStack: One member per row; empty rows are skipped.
    """
    spectra = []
    for i in range(len(tbl)):
        s = from_tara(tbl.iloc[i], flavor=flavor)
        if len(s) > 0:
            spectra.append(s)
    return SpectrumStack(spectra)
