# Loisel+2023 Hydrolight
# --------------------------------------------------------------------
def from_loisel23(ds, index, var='Rrs', units=None):
    """ Build a :class:`Spectrum` from one Loisel+2023 simulation.

    The Loisel+2023 files carry no per-variable unit metadata, so ``units``
    defaults to None; pass it explicitly if you know the convention (Rrs is
    1/sr; absorption/scattering terms are 1/m).

    Generated by JXP and Claude.

    Args:
        ds (xarray.Dataset): Dataset from
            :func:`ocpy.hydrolight.loisel23.load_ds`.
        index (int): Row index along the ``IOP_Scenario`` dimension.
        var (str): Variable name (default ``'Rrs'``; others include
            ``'a'``, ``'aph'``, ``'bb'``, ...).
        units (str, optional): Units string to record.

    Returns:
        Spectrum: Errors are None (simulations); ``metadata`` records the
            scenario index and variable.
    """
    # Lambda is a shared coordinate; the variable is (IOP_Scenario, Lambda).
    wv = np.asarray(ds['Lambda'].values, dtype=float)
    values = np.asarray(ds[var].values[index], dtype=float)
    return Spectrum(
        wavelength=wv,
        values=values,
        errors=None,
        units=units,
        source='Loisel2023',
        metadata={'scenario_index': int(index), 'variable': var},
    )


def stack_from_loisel23(ds, var='Rrs', indices=None, units=None):
    """ Build a gridded :class:`SpectrumStack` from Loisel+2023 simulations.

    All simulations share the ``Lambda`` grid, so the result is gridded.

    Generated by JXP and Claude.

    Args:
        ds (xarray.Dataset): Dataset from
            :func:`ocpy.hydrolight.loisel23.load_ds`.
        var (str): Variable name (default ``'Rrs'``).
        indices (iterable, optional): Scenario indices; defaults to all.
        units (str, optional): Units string to record on each member.

    Returns:
        SpectrumStack
    """
    nspec = ds[var].shape[0]
    if indices is None:
        indices = range(nspec)
    spectra = [from_loisel23(ds, i, var=var, units=units)
               for i in indices]
    return SpectrumStack(spectra)


