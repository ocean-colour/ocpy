""" Source adapters: build Spectrum / SpectrumStack from ocpy datasets.

These functions only *map* the output of the existing loaders
(:mod:`ocpy.insitu.pangaea`, :mod:`ocpy.hydrolight.loisel23`,
:mod:`ocpy.tara.spectra`) into the lightweight classes -- they reuse those
modules rather than re-reading files. Heavy source modules are imported
lazily inside each function so that ``import ocpy.spectra.io`` stays cheap.
"""
import numpy as np
import pandas

from ocpy.spectra.core import Spectrum, SpectrumStack


def _get(row, key):
    """ Safely fetch a scalar from a row, returning None if absent / NaN.

    Generated by JXP and Claude.

    Args:
        row (pandas.Series): A table row.
        key (str): Column name.

    Returns:
        The value, or None if the column is missing or the value is NaN.
    """
    if key in row.index:
        val = row[key]
        if pandas.notna(val):
            return val
    return None


# --------------------------------------------------------------------
# PANGAEA
# --------------------------------------------------------------------
def _pangaea_units(meta, kind):
    """ Return the unit string for a PANGAEA variable family.

    Generated by JXP and Claude.

    Args:
        meta (dict): Per-column metadata (``pangaea.column_metadata``).
        kind (str): Variable family (e.g. ``'rrs'``).

    Returns:
        str or None: The unit of the first matching spectral column.
    """
    for name, m in meta.items():
        if (m.get('variable') == kind
                and m.get('role') in ('spectral', 'spectral_band')):
            return m.get('unit')
    return None


def from_pangaea(df, obs_id, kind='rrs'):
    """ Build a :class:`Spectrum` from one PANGAEA observation.

    Generated by JXP and Claude.

    Args:
        df (pandas.DataFrame): Frame from :func:`ocpy.insitu.pangaea.load`.
        obs_id: Observation ``ID`` (index value).
        kind (str): Variable family to extract (default ``'rrs'``).

    Returns:
        Spectrum: Wavelength/value from ``pangaea.spectrum``; units from the
            column metadata; date/lat/lon/depth and any QF flags from the
            row; dataset/subdataset/contributor stashed in ``metadata``.
    """
    from ocpy.insitu import pangaea

    # Reuse the existing extractor: a wavelength-indexed Series.
    sp = pangaea.spectrum(df, obs_id, kind=kind)
    row = df.loc[obs_id]
    meta = pangaea.column_metadata(df)

    # Date -> np.datetime64 when present.
    date = _get(row, 'date_time')
    date = np.datetime64(date) if date is not None else None

    # QF flags kept separate (Q6): all columns flagged role 'qf'.
    quality = {name: row[name] for name, m in meta.items()
               if m.get('role') == 'qf' and name in row.index}

    # Provenance comment columns -> metadata.
    metadata = {name: row[name] for name, m in meta.items()
                if m.get('role') == 'provenance' and name in row.index}
    metadata['obs_id'] = obs_id
    metadata['kind'] = kind

    return Spectrum(
        wavelength=sp.index.values.astype(float),
        values=sp.values.astype(float),
        errors=None,
        units=_pangaea_units(meta, kind),
        source='PANGAEA',
        date=date,
        lat=_get(row, 'lat'),
        lon=_get(row, 'lon'),
        depth=_get(row, 'depth_m'),
        quality=quality or None,
        metadata=metadata,
    )


def stack_from_pangaea(df, kind='rrs', ids=None):
    """ Build a :class:`SpectrumStack` from PANGAEA observations.

    Generated by JXP and Claude.

    Args:
        df (pandas.DataFrame): Frame from :func:`ocpy.insitu.pangaea.load`.
        kind (str): Variable family (default ``'rrs'``).
        ids (iterable, optional): Observation IDs; defaults to all rows.

    Returns:
        SpectrumStack: One member per observation; observations with no valid
            points are skipped (so the stack is generally ragged).
    """
    if ids is None:
        ids = df.index
    spectra = []
    for obs in ids:
        s = from_pangaea(df, obs, kind=kind)
        if len(s) > 0:  # skip observations with no valid samples
            spectra.append(s)
    return SpectrumStack(spectra)


